CREATE OR REPLACE FUNCTION inventory.assert_reservation_integrity()
RETURNS void
LANGUAGE plpgsql
AS $$
DECLARE
    v_mismatch_count integer;
    v_negative_available_count integer;
    v_invalid_terminal_count integer;
BEGIN
    /*
     * Check cached reserved balances against active reservation totals.
     */
    SELECT COUNT(*)
    INTO v_mismatch_count
    FROM inventory.reservation_reconciliation
    WHERE is_reconciled = false;

    IF v_mismatch_count > 0 THEN
        RAISE EXCEPTION
            'Reservation integrity check failed: % warehouse inventory balance(s) are not reconciled',
            v_mismatch_count
            USING ERRCODE = 'P0001';
    END IF;

    /*
     * Available inventory must never be negative.
     */
    SELECT COUNT(*)
    INTO v_negative_available_count
    FROM public.warehouse_inventory
    WHERE quantity_on_hand - quantity_reserved < 0;

    IF v_negative_available_count > 0 THEN
        RAISE EXCEPTION
            'Reservation integrity check failed: % warehouse inventory row(s) have negative availability',
            v_negative_available_count
            USING ERRCODE = 'P0001';
    END IF;

    /*
     * Terminal reservations must have exactly the matching terminal
     * timestamp populated.
     */
    SELECT COUNT(*)
    INTO v_invalid_terminal_count
    FROM inventory.reservations
    WHERE
        (
            status_code = 'fulfilled'
            AND fulfilled_at IS NULL
        )
        OR
        (
            status_code = 'released'
            AND released_at IS NULL
        )
        OR
        (
            status_code = 'cancelled'
            AND cancelled_at IS NULL
        )
        OR
        (
            status_code = 'expired'
            AND expired_at IS NULL
        )
        OR
        (
            status_code = 'active'
            AND num_nonnulls(
                fulfilled_at,
                released_at,
                cancelled_at,
                expired_at
            ) <> 0
        );

    IF v_invalid_terminal_count > 0 THEN
        RAISE EXCEPTION
            'Reservation integrity check failed: % reservation row(s) have invalid status timestamps',
            v_invalid_terminal_count
            USING ERRCODE = 'P0001';
    END IF;

    RAISE NOTICE
        'Reservation integrity check passed.';
END;
$$;

