BEGIN;

CREATE SEQUENCE inventory.reservation_code_seq
    AS bigint
    START WITH 1
    INCREMENT BY 1
    MINVALUE 1
    NO MAXVALUE
    CACHE 20;


CREATE OR REPLACE FUNCTION inventory.generate_reservation_code()
RETURNS varchar
LANGUAGE sql
VOLATILE
AS $$
    SELECT
        'RSV-' ||
        lpad(
            nextval('inventory.reservation_code_seq')::text,
            9,
            '0'
        );
$$;


CREATE TABLE inventory.reservations (
    reservation_id bigint
        GENERATED BY DEFAULT AS IDENTITY,

    reservation_code varchar(20)
        NOT NULL
        DEFAULT inventory.generate_reservation_code(),

    warehouse_inventory_id integer NOT NULL,

    quantity integer NOT NULL,

    status_code varchar(30)
        NOT NULL
        DEFAULT 'active',

    source_type_code varchar(40) NOT NULL,

    source_id bigint,
    source_line_id bigint,

    expires_at timestamp without time zone,

    fulfilled_at timestamp without time zone,
    released_at timestamp without time zone,
    cancelled_at timestamp without time zone,
    expired_at timestamp without time zone,

    created_at timestamp without time zone
        NOT NULL
        DEFAULT CURRENT_TIMESTAMP,

    updated_at timestamp without time zone
        NOT NULL
        DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT reservations_pkey
        PRIMARY KEY (reservation_id),

    CONSTRAINT reservations_reservation_code_unique
        UNIQUE (reservation_code),

    CONSTRAINT reservations_warehouse_inventory_fkey
        FOREIGN KEY (warehouse_inventory_id)
        REFERENCES public.warehouse_inventory (warehouse_inventory_id)
        ON UPDATE RESTRICT
        ON DELETE RESTRICT,

    CONSTRAINT reservations_status_code_fkey
        FOREIGN KEY (status_code)
        REFERENCES inventory.reservation_statuses (status_code)
        ON UPDATE RESTRICT
        ON DELETE RESTRICT,

    CONSTRAINT reservations_source_type_code_fkey
        FOREIGN KEY (source_type_code)
        REFERENCES inventory.reservation_source_types (source_type_code)
        ON UPDATE RESTRICT
        ON DELETE RESTRICT,

    CONSTRAINT reservations_reservation_code_format
        CHECK (
            reservation_code ~ '^RSV-[0-9]{9}$'
        ),

    CONSTRAINT reservations_quantity_positive
        CHECK (quantity > 0),

    CONSTRAINT reservations_source_id_positive
        CHECK (
            source_id IS NULL
            OR source_id > 0
        ),

    CONSTRAINT reservations_source_line_id_positive
        CHECK (
            source_line_id IS NULL
            OR source_line_id > 0
        ),

    CONSTRAINT reservations_expiration_after_creation
        CHECK (
            expires_at IS NULL
            OR expires_at > created_at
        ),

    CONSTRAINT reservations_updated_not_before_created
        CHECK (
            updated_at >= created_at
        ),

    CONSTRAINT reservations_fulfilled_not_before_created
        CHECK (
            fulfilled_at IS NULL
            OR fulfilled_at >= created_at
        ),

    CONSTRAINT reservations_released_not_before_created
        CHECK (
            released_at IS NULL
            OR released_at >= created_at
        ),

    CONSTRAINT reservations_cancelled_not_before_created
        CHECK (
            cancelled_at IS NULL
            OR cancelled_at >= created_at
        ),

    CONSTRAINT reservations_expired_not_before_created
        CHECK (
            expired_at IS NULL
            OR expired_at >= created_at
        ),

    CONSTRAINT reservations_single_terminal_timestamp
        CHECK (
            num_nonnulls(
                fulfilled_at,
                released_at,
                cancelled_at,
                expired_at
            ) <= 1
        ),

    CONSTRAINT reservations_status_timestamp_consistency
        CHECK (
            (
                status_code = 'active'
                AND fulfilled_at IS NULL
                AND released_at IS NULL
                AND cancelled_at IS NULL
                AND expired_at IS NULL
            )
            OR
            (
                status_code = 'fulfilled'
                AND fulfilled_at IS NOT NULL
                AND released_at IS NULL
                AND cancelled_at IS NULL
                AND expired_at IS NULL
            )
            OR
            (
                status_code = 'released'
                AND fulfilled_at IS NULL
                AND released_at IS NOT NULL
                AND cancelled_at IS NULL
                AND expired_at IS NULL
            )
            OR
            (
                status_code = 'cancelled'
                AND fulfilled_at IS NULL
                AND released_at IS NULL
                AND cancelled_at IS NOT NULL
                AND expired_at IS NULL
            )
            OR
            (
                status_code = 'expired'
                AND fulfilled_at IS NULL
                AND released_at IS NULL
                AND cancelled_at IS NULL
                AND expired_at IS NOT NULL
            )
        )
);


CREATE INDEX reservations_warehouse_inventory_idx
    ON inventory.reservations (warehouse_inventory_id);


CREATE INDEX reservations_active_inventory_idx
    ON inventory.reservations (warehouse_inventory_id)
    WHERE status_code = 'active';


CREATE INDEX reservations_source_reference_idx
    ON inventory.reservations (
        source_type_code,
        source_id,
        source_line_id
    );


CREATE INDEX reservations_active_expiration_idx
    ON inventory.reservations (expires_at)
    WHERE status_code = 'active'
      AND expires_at IS NOT NULL;


COMMIT;

