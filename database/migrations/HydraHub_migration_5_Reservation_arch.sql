CREATE TABLE inventory.reservation_events (
    reservation_event_id bigint
        GENERATED BY DEFAULT AS IDENTITY,

    reservation_id bigint NOT NULL,

    event_type varchar(40) NOT NULL,

    previous_status_code varchar(30),
    new_status_code varchar(30),

    previous_quantity integer,
    new_quantity integer,

    reason text,
    metadata jsonb,

    created_at timestamp without time zone
        NOT NULL
        DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT reservation_events_pkey
        PRIMARY KEY (reservation_event_id),

    CONSTRAINT reservation_events_reservation_id_fkey
        FOREIGN KEY (reservation_id)
        REFERENCES inventory.reservations (reservation_id)
        ON UPDATE RESTRICT
        ON DELETE RESTRICT,

    CONSTRAINT reservation_events_previous_status_fkey
        FOREIGN KEY (previous_status_code)
        REFERENCES inventory.reservation_statuses (status_code)
        ON UPDATE RESTRICT
        ON DELETE RESTRICT,

    CONSTRAINT reservation_events_new_status_fkey
        FOREIGN KEY (new_status_code)
        REFERENCES inventory.reservation_statuses (status_code)
        ON UPDATE RESTRICT
        ON DELETE RESTRICT,

    CONSTRAINT reservation_events_event_type_not_blank
        CHECK (btrim(event_type) <> ''),

    CONSTRAINT reservation_events_event_type_format
        CHECK (
            event_type = lower(event_type)
            AND event_type ~ '^[a-z][a-z0-9_]*$'
        ),

    CONSTRAINT reservation_events_previous_quantity_positive
        CHECK (
            previous_quantity IS NULL
            OR previous_quantity > 0
        ),

    CONSTRAINT reservation_events_new_quantity_positive
        CHECK (
            new_quantity IS NULL
            OR new_quantity > 0
        ),

    CONSTRAINT reservation_events_reason_not_blank
        CHECK (
            reason IS NULL
            OR btrim(reason) <> ''
        ),

    CONSTRAINT reservation_events_created_values_required
        CHECK (
            event_type <> 'created'
            OR (
                previous_status_code IS NULL
                AND new_status_code = 'active'
                AND previous_quantity IS NULL
                AND new_quantity IS NOT NULL
            )
        ),

    CONSTRAINT reservation_events_status_change_values_required
        CHECK (
            event_type NOT IN (
                'fulfilled',
                'released',
                'cancelled',
                'expired'
            )
            OR (
                previous_status_code = 'active'
                AND new_status_code = event_type
            )
        ),

    CONSTRAINT reservation_events_quantity_adjusted_values_required
        CHECK (
            event_type <> 'quantity_adjusted'
            OR (
                previous_quantity IS NOT NULL
                AND new_quantity IS NOT NULL
                AND previous_quantity <> new_quantity
            )
        )
);


CREATE INDEX reservation_events_reservation_created_idx
    ON inventory.reservation_events (
        reservation_id,
        created_at,
        reservation_event_id
    );


CREATE INDEX reservation_events_event_type_idx
    ON inventory.reservation_events (event_type);


CREATE INDEX reservation_events_metadata_gin_idx
    ON inventory.reservation_events
    USING gin (metadata)
    WHERE metadata IS NOT NULL;

COMMIT;

