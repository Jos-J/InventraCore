CREATE OR REPLACE FUNCTION inventory.adjust_reservation(
    p_reservation_id bigint,
    p_new_quantity integer,
    p_performed_by_user_id integer DEFAULT NULL,
    p_reason text DEFAULT NULL,
    p_metadata jsonb DEFAULT NULL
)
RETURNS inventory.reservations
LANGUAGE plpgsql
AS $$
DECLARE
    v_reservation inventory.reservations%ROWTYPE;
    v_updated_reservation inventory.reservations%ROWTYPE;

    v_quantity_on_hand integer;
    v_quantity_reserved integer;
    v_quantity_available integer;
    v_quantity_difference integer;
BEGIN
    /*
     * Validate the request.
     */
    IF p_reservation_id IS NULL THEN
        RAISE EXCEPTION
            'reservation_id is required'
            USING ERRCODE = '22004';
    END IF;

    IF p_reservation_id <= 0 THEN
        RAISE EXCEPTION
            'reservation_id must be greater than zero'
            USING ERRCODE = '22023';
    END IF;

    IF p_new_quantity IS NULL OR p_new_quantity <= 0 THEN
        RAISE EXCEPTION
            'New reservation quantity must be greater than zero'
            USING ERRCODE = '22023';
    END IF;

    IF p_reason IS NOT NULL
       AND btrim(p_reason) = '' THEN
        RAISE EXCEPTION
            'reason cannot be blank when supplied'
            USING ERRCODE = '22023';
    END IF;

    /*
     * Lock the reservation so no other lifecycle operation can modify it
     * during this adjustment.
     */
    SELECT *
    INTO v_reservation
    FROM inventory.reservations
    WHERE reservation_id = p_reservation_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'Reservation % does not exist',
            p_reservation_id
            USING ERRCODE = 'P0002';
    END IF;

    IF v_reservation.status_code <> 'active' THEN
        RAISE EXCEPTION
            'Reservation % cannot be adjusted because its current status is %',
            p_reservation_id,
            v_reservation.status_code
            USING ERRCODE = 'P0001';
    END IF;

    IF p_new_quantity = v_reservation.quantity THEN
        RAISE EXCEPTION
            'New quantity must differ from the current reservation quantity of %',
            v_reservation.quantity
            USING ERRCODE = '22023';
    END IF;

    /*
     * Lock the related inventory balance.
     */
    SELECT
        quantity_on_hand,
        quantity_reserved
    INTO
        v_quantity_on_hand,
        v_quantity_reserved
    FROM public.warehouse_inventory
    WHERE warehouse_inventory_id =
        v_reservation.warehouse_inventory_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'Warehouse inventory record % does not exist',
            v_reservation.warehouse_inventory_id
            USING ERRCODE = 'P0002';
    END IF;

    /*
     * Confirm the cached reserved balance can account for the existing
     * reservation before modifying it.
     */
    IF v_quantity_reserved < v_reservation.quantity THEN
        RAISE EXCEPTION
            'Reserved inventory balance is inconsistent for warehouse_inventory_id %. Balance: %, reservation quantity: %',
            v_reservation.warehouse_inventory_id,
            v_quantity_reserved,
            v_reservation.quantity
            USING ERRCODE = 'P0001';
    END IF;

    v_quantity_difference :=
        p_new_quantity - v_reservation.quantity;

    /*
     * A positive difference requires additional available inventory.
     * A negative difference releases part of the existing reservation.
     */
    IF v_quantity_difference > 0 THEN
        v_quantity_available :=
            v_quantity_on_hand - v_quantity_reserved;

        IF v_quantity_difference > v_quantity_available THEN
            RAISE EXCEPTION
                'Insufficient inventory to increase reservation %. Additional requested: %, available: %',
                p_reservation_id,
                v_quantity_difference,
                v_quantity_available
                USING ERRCODE = 'P0001';
        END IF;
    END IF;

    /*
     * Apply only the difference to the cached reserved balance.
     *
     * Example:
     * current reservation = 5
     * new reservation     = 8
     * difference          = 3
     */
    UPDATE public.warehouse_inventory
    SET
        quantity_reserved =
            quantity_reserved + v_quantity_difference,
        updated_at = CURRENT_TIMESTAMP
    WHERE warehouse_inventory_id =
        v_reservation.warehouse_inventory_id;

    /*
     * Update the detailed reservation record.
     */
    UPDATE inventory.reservations
    SET
        quantity = p_new_quantity,
        updated_at = CURRENT_TIMESTAMP
    WHERE reservation_id = p_reservation_id
    RETURNING *
    INTO v_updated_reservation;

    /*
     * Append the audit event.
     */
    INSERT INTO inventory.reservation_events (
        reservation_id,
        event_type,
        previous_status_code,
        new_status_code,
        previous_quantity,
        new_quantity,
        reason,
        metadata,
        performed_by_user_id
    )
    VALUES (
        p_reservation_id,
        'quantity_adjusted',
        'active',
        'active',
        v_reservation.quantity,
        p_new_quantity,
        p_reason,
        p_metadata,
        p_performed_by_user_id
    );

    RETURN v_updated_reservation;
END;
$$;

COMMIT;

