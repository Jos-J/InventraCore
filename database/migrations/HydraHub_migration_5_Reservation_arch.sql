CREATE OR REPLACE PROCEDURE inventory.reset_reservation_test_data(
    p_confirmation text
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_reservation_id_sequence text;
    v_event_id_sequence text;
BEGIN
    /*
     * Require an explicit confirmation phrase because this operation
     * permanently deletes all reservation records and events.
     */
    IF p_confirmation IS DISTINCT FROM 'RESET RESERVATION TEST DATA' THEN
        RAISE EXCEPTION
            'Reset rejected. Pass the exact confirmation phrase: RESET RESERVATION TEST DATA'
            USING ERRCODE = '22023';
    END IF;

    /*
     * Lock the affected tables so another session cannot create or modify
     * reservations while the reset is running.
     */
    LOCK TABLE inventory.reservation_events
        IN ACCESS EXCLUSIVE MODE;

    LOCK TABLE inventory.reservations
        IN ACCESS EXCLUSIVE MODE;

    LOCK TABLE public.warehouse_inventory
        IN ACCESS EXCLUSIVE MODE;

    /*
     * Delete children before parents because reservation_events has a
     * foreign key to reservations.
     */
    DELETE FROM inventory.reservation_events;

    DELETE FROM inventory.reservations;

    /*
     * Restore the cached reserved balance to a clean test baseline.
     */
    UPDATE public.warehouse_inventory
    SET
        quantity_reserved = 0,
        updated_at = CURRENT_TIMESTAMP
    WHERE quantity_reserved <> 0;

    /*
     * Locate the identity sequences generated for the primary keys.
     */
    v_reservation_id_sequence :=
        pg_get_serial_sequence(
            'inventory.reservations',
            'reservation_id'
        );

    v_event_id_sequence :=
        pg_get_serial_sequence(
            'inventory.reservation_events',
            'reservation_event_id'
        );

    /*
     * Reset identity values so the next generated value is 1.
     */
    IF v_reservation_id_sequence IS NOT NULL THEN
        PERFORM setval(
            v_reservation_id_sequence::regclass,
            1,
            false
        );
    END IF;

    IF v_event_id_sequence IS NOT NULL THEN
        PERFORM setval(
            v_event_id_sequence::regclass,
            1,
            false
        );
    END IF;

    /*
     * Reset the separate human-readable reservation-code sequence.
     */
    PERFORM setval(
        'inventory.reservation_code_seq'::regclass,
        1,
        false
    );

    RAISE NOTICE
        'Reservation test data reset successfully.';
END;
$$;

COMMIT;

