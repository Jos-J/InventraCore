CREATE OR REPLACE FUNCTION inventory.create_reservation(
    p_warehouse_inventory_id integer,
    p_quantity integer,
    p_source_type_code varchar,
    p_source_id bigint DEFAULT NULL,
    p_source_line_id bigint DEFAULT NULL,
    p_expires_at timestamp without time zone DEFAULT NULL,
    p_created_by_user_id integer DEFAULT NULL,
    p_reason text DEFAULT NULL,
    p_metadata jsonb DEFAULT NULL
)
RETURNS inventory.reservations
LANGUAGE plpgsql
AS $$
DECLARE
    v_quantity_on_hand integer;
    v_quantity_reserved integer;
    v_quantity_available integer;

    v_requires_source_id boolean;
    v_requires_source_line_id boolean;
    v_source_type_active boolean;

    v_reservation inventory.reservations%ROWTYPE;
BEGIN
    /*
     * Validate the basic request.
     */
    IF p_warehouse_inventory_id IS NULL THEN
        RAISE EXCEPTION
            'warehouse_inventory_id is required'
            USING ERRCODE = '22004';
    END IF;

    IF p_quantity IS NULL OR p_quantity <= 0 THEN
        RAISE EXCEPTION
            'Reservation quantity must be greater than zero'
            USING ERRCODE = '22023';
    END IF;

    IF p_source_type_code IS NULL
       OR btrim(p_source_type_code) = '' THEN
        RAISE EXCEPTION
            'source_type_code is required'
            USING ERRCODE = '22023';
    END IF;

    IF p_source_id IS NOT NULL AND p_source_id <= 0 THEN
        RAISE EXCEPTION
            'source_id must be greater than zero when supplied'
            USING ERRCODE = '22023';
    END IF;

    IF p_source_line_id IS NOT NULL
       AND p_source_line_id <= 0 THEN
        RAISE EXCEPTION
            'source_line_id must be greater than zero when supplied'
            USING ERRCODE = '22023';
    END IF;

    IF p_expires_at IS NOT NULL
       AND p_expires_at <= CURRENT_TIMESTAMP THEN
        RAISE EXCEPTION
            'expires_at must be later than the current timestamp'
            USING ERRCODE = '22023';
    END IF;

    IF p_reason IS NOT NULL
       AND btrim(p_reason) = '' THEN
        RAISE EXCEPTION
            'reason cannot be blank when supplied'
            USING ERRCODE = '22023';
    END IF;

    /*
     * Validate the source type and determine which identifiers it requires.
     */
    SELECT
        requires_source_id,
        requires_source_line_id,
        is_active
    INTO
        v_requires_source_id,
        v_requires_source_line_id,
        v_source_type_active
    FROM inventory.reservation_source_types
    WHERE source_type_code = lower(btrim(p_source_type_code));

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'Unknown reservation source type: %',
            p_source_type_code
            USING ERRCODE = '22023';
    END IF;

    IF NOT v_source_type_active THEN
        RAISE EXCEPTION
            'Reservation source type % is inactive',
            p_source_type_code
            USING ERRCODE = '22023';
    END IF;

    IF v_requires_source_id
       AND p_source_id IS NULL THEN
        RAISE EXCEPTION
            'Source type % requires source_id',
            p_source_type_code
            USING ERRCODE = '22023';
    END IF;

    IF v_requires_source_line_id
       AND p_source_line_id IS NULL THEN
        RAISE EXCEPTION
            'Source type % requires source_line_id',
            p_source_type_code
            USING ERRCODE = '22023';
    END IF;

    /*
     * Lock the inventory row.
     *
     * Concurrent reservation requests for the same row must wait here,
     * preventing both transactions from reserving the same availability.
     */
    SELECT
        quantity_on_hand,
        quantity_reserved
    INTO
        v_quantity_on_hand,
        v_quantity_reserved
    FROM public.warehouse_inventory
    WHERE warehouse_inventory_id = p_warehouse_inventory_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'Warehouse inventory record % does not exist',
            p_warehouse_inventory_id
            USING ERRCODE = 'P0002';
    END IF;

    v_quantity_available :=
        v_quantity_on_hand - v_quantity_reserved;

    IF p_quantity > v_quantity_available THEN
        RAISE EXCEPTION
            'Insufficient inventory for warehouse_inventory_id %. Requested: %, available: %',
            p_warehouse_inventory_id,
            p_quantity,
            v_quantity_available
            USING ERRCODE = 'P0001';
    END IF;

    /*
     * Create the detailed reservation record.
     */
    INSERT INTO inventory.reservations (
        warehouse_inventory_id,
        quantity,
        status_code,
        source_type_code,
        source_id,
        source_line_id,
        expires_at,
        created_by_user_id
    )
    VALUES (
        p_warehouse_inventory_id,
        p_quantity,
        'active',
        lower(btrim(p_source_type_code)),
        p_source_id,
        p_source_line_id,
        p_expires_at,
        p_created_by_user_id
    )
    RETURNING *
    INTO v_reservation;

    /*
     * Update the fast current inventory balance.
     */
    UPDATE public.warehouse_inventory
    SET
        quantity_reserved = quantity_reserved + p_quantity,
        updated_at = CURRENT_TIMESTAMP
    WHERE warehouse_inventory_id = p_warehouse_inventory_id;

    /*
     * Append the immutable creation event.
     */
    INSERT INTO inventory.reservation_events (
        reservation_id,
        event_type,
        previous_status_code,
        new_status_code,
        previous_quantity,
        new_quantity,
        reason,
        metadata,
        performed_by_user_id
    )
    VALUES (
        v_reservation.reservation_id,
        'created',
        NULL,
        'active',
        NULL,
        p_quantity,
        p_reason,
        p_metadata,
        p_created_by_user_id
    );

    RETURN v_reservation;
END;
$$;

COMMIT;
