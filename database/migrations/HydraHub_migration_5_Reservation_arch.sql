--------005l_create_reservation_reconciliation_view
----------------------------------------------------------

BEGIN;

CREATE OR REPLACE VIEW inventory.reservation_reconciliation AS
SELECT
    wi.warehouse_inventory_id,
    wi.warehouse_id,
    wi.variant_id,
    wi.quantity_on_hand,
    wi.quantity_reserved AS cached_quantity_reserved,

    COALESCE(
        SUM(r.quantity) FILTER (
            WHERE r.status_code = 'active'
        ),
        0
    )::integer AS calculated_quantity_reserved,

    wi.quantity_reserved
    -
    COALESCE(
        SUM(r.quantity) FILTER (
            WHERE r.status_code = 'active'
        ),
        0
    )::integer AS reserved_quantity_difference,

    wi.quantity_on_hand - wi.quantity_reserved
        AS cached_quantity_available,

    wi.quantity_on_hand
    -
    COALESCE(
        SUM(r.quantity) FILTER (
            WHERE r.status_code = 'active'
        ),
        0
    )::integer AS calculated_quantity_available,

    (
        wi.quantity_reserved
        =
        COALESCE(
            SUM(r.quantity) FILTER (
                WHERE r.status_code = 'active'
            ),
            0
        )::integer
    ) AS is_reconciled

FROM public.warehouse_inventory AS wi

LEFT JOIN inventory.reservations AS r
    ON r.warehouse_inventory_id =
       wi.warehouse_inventory_id

GROUP BY
    wi.warehouse_inventory_id,
    wi.warehouse_id,
    wi.variant_id,
    wi.quantity_on_hand,
    wi.quantity_reserved;

COMMIT;

