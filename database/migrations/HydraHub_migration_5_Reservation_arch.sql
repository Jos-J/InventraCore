------005f_release_reservation_function
--------------------------------------------
BEGIN;

CREATE OR REPLACE FUNCTION inventory.release_reservation(
    p_reservation_id bigint,
    p_performed_by_user_id integer DEFAULT NULL,
    p_reason text DEFAULT NULL,
    p_metadata jsonb DEFAULT NULL
)
RETURNS inventory.reservations
LANGUAGE plpgsql
AS $$
DECLARE
    v_reservation inventory.reservations%ROWTYPE;
    v_updated_reservation inventory.reservations%ROWTYPE;
    v_current_quantity_reserved integer;
BEGIN
    /*
     * Validate the request.
     */
    IF p_reservation_id IS NULL THEN
        RAISE EXCEPTION
            'reservation_id is required'
            USING ERRCODE = '22004';
    END IF;

    IF p_reservation_id <= 0 THEN
        RAISE EXCEPTION
            'reservation_id must be greater than zero'
            USING ERRCODE = '22023';
    END IF;

    IF p_reason IS NOT NULL
       AND btrim(p_reason) = '' THEN
        RAISE EXCEPTION
            'reason cannot be blank when supplied'
            USING ERRCODE = '22023';
    END IF;

    /*
     * Lock the reservation so two sessions cannot release or otherwise
     * complete the same reservation simultaneously.
     */
    SELECT *
    INTO v_reservation
    FROM inventory.reservations
    WHERE reservation_id = p_reservation_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'Reservation % does not exist',
            p_reservation_id
            USING ERRCODE = 'P0002';
    END IF;

    IF v_reservation.status_code <> 'active' THEN
        RAISE EXCEPTION
            'Reservation % cannot be released because its current status is %',
            p_reservation_id,
            v_reservation.status_code
            USING ERRCODE = 'P0001';
    END IF;

    /*
     * Lock the related inventory balance.
     */
    SELECT quantity_reserved
    INTO v_current_quantity_reserved
    FROM public.warehouse_inventory
    WHERE warehouse_inventory_id =
        v_reservation.warehouse_inventory_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'Warehouse inventory record % does not exist',
            v_reservation.warehouse_inventory_id
            USING ERRCODE = 'P0002';
    END IF;

    /*
     * This should never fail during normal operation. It protects against
     * existing balance drift or uncontrolled direct updates.
     */
    IF v_current_quantity_reserved < v_reservation.quantity THEN
        RAISE EXCEPTION
            'Reserved inventory balance is inconsistent for warehouse_inventory_id %. Balance: %, reservation quantity: %',
            v_reservation.warehouse_inventory_id,
            v_current_quantity_reserved,
            v_reservation.quantity
            USING ERRCODE = 'P0001';
    END IF;

    /*
     * Return the reserved quantity to availability.
     */
    UPDATE public.warehouse_inventory
    SET
        quantity_reserved =
            quantity_reserved - v_reservation.quantity,
        updated_at = CURRENT_TIMESTAMP
    WHERE warehouse_inventory_id =
        v_reservation.warehouse_inventory_id;

    /*
     * Move the reservation into its terminal released state.
     */
    UPDATE inventory.reservations
    SET
        status_code = 'released',
        released_at = CURRENT_TIMESTAMP,
        updated_at = CURRENT_TIMESTAMP
    WHERE reservation_id = p_reservation_id
    RETURNING *
    INTO v_updated_reservation;

    /*
     * Append the audit event.
     */
    INSERT INTO inventory.reservation_events (
        reservation_id,
        event_type,
        previous_status_code,
        new_status_code,
        previous_quantity,
        new_quantity,
        reason,
        metadata,
        performed_by_user_id
    )
    VALUES (
        p_reservation_id,
        'released',
        'active',
        'released',
        v_reservation.quantity,
        v_reservation.quantity,
        p_reason,
        p_metadata,
        p_performed_by_user_id
    );

    RETURN v_updated_reservation;
END;
$$;

COMMIT;

